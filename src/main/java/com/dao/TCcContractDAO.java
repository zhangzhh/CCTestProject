package com.dao;

import java.util.List;

import org.hibernate.LockOptions;
import org.hibernate.Query;
import org.hibernate.criterion.Example;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import com.common.BaseHibernateDAO;
import com.entity.TCcContract;

/**
 	* A data access object (DAO) providing persistence and search support for TCcContract entities.
 			* Transaction control of the save(), update() and delete() operations 
		can directly support Spring container-managed transactions or they can be augmented	to handle user-managed Spring transactions. 
		Each of these methods provides additional information for how to configure it for the desired type of transaction control. 	
	 * @see com.dao.TCcContract
  * @author MyEclipse Persistence Tools 
 */
public class TCcContractDAO extends BaseHibernateDAO  {
	     private static final Logger log = LoggerFactory.getLogger(TCcContractDAO.class);
	

    
    public void save(TCcContract transientInstance) {
        log.debug("saving TCcContract instance");
        try {
            getSession().save(transientInstance);
            log.debug("save successful");
        } catch (RuntimeException re) {
            log.error("save failed", re);
            throw re;
        }
    }
    
	public void delete(TCcContract persistentInstance) {
        log.debug("deleting TCcContract instance");
        try {
            getSession().delete(persistentInstance);
            log.debug("delete successful");
        } catch (RuntimeException re) {
            log.error("delete failed", re);
            throw re;
        }
    }
    
    public TCcContract findById( java.lang.Long id) {
        log.debug("getting TCcContract instance with id: " + id);
        try {
            TCcContract instance = (TCcContract) getSession()
                    .get("com.dao.TCcContract", id);
            return instance;
        } catch (RuntimeException re) {
            log.error("get failed", re);
            throw re;
        }
    }
    
    
    public List findByExample(TCcContract instance) {
        log.debug("finding TCcContract instance by example");
        try {
            List results = getSession()
                    .createCriteria("com.dao.TCcContract")
                    .add(Example.create(instance))
            .list();
            log.debug("find by example successful, result size: " + results.size());
            return results;
        } catch (RuntimeException re) {
            log.error("find by example failed", re);
            throw re;
        }
    }    
    
    public List findByProperty(String propertyName, Object value) {
      log.debug("finding TCcContract instance with property: " + propertyName
            + ", value: " + value);
      try {
         String queryString = "from TCcContract as model where model." 
         						+ propertyName + "= ?";
         Query queryObject = getSession().createQuery(queryString);
		 queryObject.setParameter(0, value);
		 return queryObject.list();
      } catch (RuntimeException re) {
         log.error("find by property name failed", re);
         throw re;
      }
	}


	public List findAll() {
		log.debug("finding all TCcContract instances");
		try {
			String queryString = "from TCcContract";
	         Query queryObject = getSession().createQuery(queryString);
			 return queryObject.list();
		} catch (RuntimeException re) {
			log.error("find all failed", re);
			throw re;
		}
	}
	
    public TCcContract merge(TCcContract detachedInstance) {
        log.debug("merging TCcContract instance");
        try {
            TCcContract result = (TCcContract) getSession()
                    .merge(detachedInstance);
            log.debug("merge successful");
            return result;
        } catch (RuntimeException re) {
            log.error("merge failed", re);
            throw re;
        }
    }

    public void attachDirty(TCcContract instance) {
        log.debug("attaching dirty TCcContract instance");
        try {
            getSession().saveOrUpdate(instance);
            log.debug("attach successful");
        } catch (RuntimeException re) {
            log.error("attach failed", re);
            throw re;
        }
    }
    
    public void attachClean(TCcContract instance) {
        log.debug("attaching clean TCcContract instance");
        try {
                      	getSession().buildLockRequest(LockOptions.NONE).lock(instance);
          	            log.debug("attach successful");
        } catch (RuntimeException re) {
            log.error("attach failed", re);
            throw re;
        }
    }
}